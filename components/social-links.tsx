function SocialLink({ link, isPrimary = false }: { link: (typeof primaryLinks)[0]; isPrimary?: boolean }) {
  const [isHovered, setIsHovered] = useState(false)

  return (
    <motion.a
      href={link.href}
      target="_blank"
      rel="noopener noreferrer"
      className="relative"
      onHoverStart={() => setIsHovered(true)}
      onHoverEnd={() => setIsHovered(false)}
      initial={{ y: 0 }}
      animate={{ y: 0 }}
      whileHover={{
        y: [0, -8, 0],
        transition: {
          duration: 0.4,
          ease: "easeInOut",
        },
      }}
    >
      <div
        className={`flex items-center justify-center rounded-full bg-foreground/10 text-foreground transition-colors hover:bg-[#2563EB] hover:text-white ${
          isPrimary ? "h-16 w-16" : "h-12 w-12"
        }`}
      >
        <div className={isPrimary ? "h-8 w-8" : "h-6 w-6"}>{link.icon}</div>
      </div>

      {/* Tooltip */}
      <motion.div
        initial={{ opacity: 0, y: 8 }}
        animate={{ opacity: isHovered ? 1 : 0, y: isHovered ? 0 : 8 }}
        className="pointer-events-none absolute left-1/2 top-full mt-2 -translate-x-1/2 whitespace-nowrap rounded bg-[#2563EB] px-2 py-1 text-xs font-medium text-white"
      >
        {link.name}
        {/* Arrow */}
        <div className="absolute -top-1 left-1/2 h-2 w-2 -translate-x-1/2 rotate-45 bg-[#2563EB]" />
      </motion.div>
    </motion.a>
  )
}

